//initialize variable 'x' with value 4.
let x:number = 4;
//Compound Addition Assignment(+=).Add 4 to the current value of x and assign the result back to x. 
console.log(x+= 4);//8
//Compound Subtraction Assignment(-=).Subtract 4 from the current value of x and assign the result back to x.
console.log(x-=4);//4
//Compunt Multiplication Assignment(*=).Multiply 2 by the current value of x and assign the result bak to x.
console.log(x*=2);//8
//Compound Division Assignment(/=).Divide the current value of x by 2 and assign the result back to x.
console.log(x/=2);//4
